async function populateData() {
  try {
    console.log(' Starting data population...\n');

    // Step 1: Clear existing data
    console.log('  Clearing existing connectors...');
    await Connector.destroy({ where: {}, force: true });
    console.log(' Connectors cleared');

    console.log('  Clearing existing communities...');
    await Community.destroy({ where: {}, force: true });
    console.log(' Communities cleared\n');

    // Step 2: Insert communities
    console.log(' Inserting communities...');
    for (const community of communitiesData) {
      await Community.create({
        ...community,
        createdAt: new Date(),
        updatedAt: new Date(),
      });
      console.log(`    ${community.name} (${community.communityInterest})`);
    }
    console.log(`\n ${communitiesData.length} communities created\n`);

    // Step 3: Insert connectors
    console.log(' Inserting connectors...');
    let connectorCount = 0;
    for (const connector of connectorsData) {
      await Connector.create({
        ...connector,
        createdAt: new Date(),
        updatedAt: new Date(),
      });
      connectorCount++;
      if (connectorCount % 12 === 0) {
        console.log(`    ${connectorCount} connectors created...`);
      }
    }
    console.log(`\n ${connectorsData.length} connectors created\n`);

    // Summary
    console.log('');
    console.log(' POPULATION SUMMARY');
    console.log('');
    console.log(`Communities: ${communitiesData.length} (6 per category  12 categories)`);
    console.log(`Connectors: ${connectorsData.length} (6 per community  ${communitiesData.length} communities)`);
    console.log('Categories covered: 12');
    console.log('');
    console.log('\n Data population complete!');

    process.exit(0);
  } catch (error) {
    console.error(' Error populating data:', error);
    process.exit(1);
  }
}

